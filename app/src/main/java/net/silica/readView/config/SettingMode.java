package net.silica.readView.config;

public class SettingMode {
    String bgColor;
    String textColor;
    String fontFamily;
    int fontSize;
    int paddingLeftRight;
    int paddingTopBottom;
    int columnWidth;
    int height;
    int columnGap;
    String fontWeight;
    String fontStyle;
    int width;

    public SettingMode() {
    }

    public String getBgColor() {
        return bgColor;
    }

    public void setBgColor(String bgColor) {
        this.bgColor = bgColor;
    }

    public String getTextColor() {
        return textColor;
    }

    public void setTextColor(String textColor) {
        this.textColor = textColor;
    }

    public String getFontFamily() {
        return fontFamily;
    }

    public void setFontFamily(String fontFamily) {
        this.fontFamily = fontFamily;
    }

    public int getfontSize() {
        return fontSize;
    }

    public void setfontSize(int fontSize) {
        this.fontSize = fontSize;
    }

    public int getPaddingLeftRight() {
        return paddingLeftRight;
    }

    public void setPaddingLeftRight(int paddingLeftRight) {
        this.paddingLeftRight = paddingLeftRight;
    }

    public int getPaddingTopBottom() {
        return paddingTopBottom;
    }

    public void setPaddingTopBottom(int paddingTopBottom) {
        this.paddingTopBottom = paddingTopBottom;
    }

    public int getColumnWidth() {
        return columnWidth;
    }

    public void setColumnWidth(int columnWidth) {
        this.columnWidth = columnWidth;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getColumnGap() {
        return columnGap;
    }

    public void setColumnGap(int columnGap) {
        this.columnGap = columnGap;
    }

    public String getFontWeight() {
        return fontWeight;
    }

    public void setFontWeight(String fontWeight) {
        this.fontWeight = fontWeight;
    }

    public String getFontStyle() {
        return fontStyle;
    }

    public void setFontStyle(String fontStyle) {
        this.fontStyle = fontStyle;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }
}
